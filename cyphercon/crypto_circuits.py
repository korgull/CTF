
def circuit(input):
    A = int(format(input, '008b')[0:1])
    B = int(format(input, '008b')[1:2])
    C = int(format(input, '008b')[2:3])
    D = int(format(input, '008b')[3:4])
    E = int(format(input, '008b')[4:5])
    F = int(format(input, '008b')[5:6])
    G = int(format(input, '008b')[6:7])
    H = int(format(input, '008b')[7:8])

    X1 = B ^ C ^ D
    X2 = F ^ G ^ H
    X5 = X1 ^ E ^ X2
    X3 = C ^ X5
    X6 = X3 ^ D
    X8 = X6 ^ E
    X4 = X8 ^ F
    X7 = X4 ^ G
    X9 = X7 ^ H

    output = int(bin((A << 7) + (X5 << 6) + (X3 << 5) + (X6 << 4) + (X8 << 3) + (X4 << 2) + (X7 << 1) + X9),2)
    return chr(output)


